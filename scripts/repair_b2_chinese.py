def main():
    bank_path = Path("question_bank_b2.json")
    missing_path = Path("question_bank_b2_missing_chinese.csv")
    translations_path = Path("b2_ch_translations.json")

    bank = json.loads(bank_path.read_text(encoding="utf-8"))

    updated = 0
    for item in bank:
        word = item.get("word")
        if word in TRANSLATIONS:
            item["ch"] = TRANSLATIONS[word]
            updated += 1

    bank_path.write_text(json.dumps(bank, ensure_ascii=False, indent=2), encoding="utf-8")

    existing = {}
    if translations_path.exists():
        existing = json.loads(translations_path.read_text(encoding="utf-8"))
    existing.update(TRANSLATIONS)
    translations_path.write_text(json.dumps(dict(sorted(existing.items())), ensure_ascii=False, indent=2), encoding="utf-8")

    missing_rows = []
    for item in bank:
        ch = (item.get("ch") or "").strip()
        if not ch or ch in {"?", "??"}:
            missing_rows.append({
                "word": item["word"],
                "cefr": item.get("cefr", "B2"),
                "frequency": item.get("frequency", ""),
            })

    with missing_path.open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=["word", "cefr", "frequency"])
        writer.writeheader()
        writer.writerows(missing_rows)

    print("updated", updated)
    print("with_ch", sum(1 for item in bank if (item.get("ch") or "").strip() not in {"", "?", "??"}))
    print("missing_ch", len(missing_rows))


if __name__ == "__main__":
    main()
