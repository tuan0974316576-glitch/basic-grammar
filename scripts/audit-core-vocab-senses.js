async function auditStudentNearDuplicateMeaningMetadata(words = null, providers = getStudentLookupProviders()) {
  const sourceWords = Array.isArray(words)
    ? getUniqueAuditWords(words)
    : getApprovedLookupAuditWords();
  const nearDuplicateMeanings = [];
  let checkedEntryCount = 0;

  for (const word of sourceWords) {
    const matches = await buildStudentLookupMatches(word, providers);
    checkedEntryCount += matches.length;
    const grouped = new Map();
    matches.forEach((entry) => {
      const key = [
        teacherVocab.normalizePos(entry.pos || entry.inferredPos) || String(entry.type || ""),
        teacherVocab.normalizeType(entry.type, entry.word || entry.display || word)
      ].join("\t");
      if (!grouped.has(key)) grouped.set(key, []);
      grouped.get(key).push(entry);
    });

    grouped.forEach((group, key) => {
      for (let leftIndex = 0; leftIndex < group.length; leftIndex += 1) {
        for (let rightIndex = leftIndex + 1; rightIndex < group.length; rightIndex += 1) {
          const left = group[leftIndex];
          const right = group[rightIndex];
          const overlap = getMeaningPartOverlap(left.meaning, right.meaning);
          if (!overlap.isNearDuplicate) continue;
          nearDuplicateMeanings.push([
            word,
            key,
            `${left.meaning} (${left.source || ""})`,
            `${right.meaning} (${right.source || ""})`,
            overlap.reason
          ].join(" :: "));
        }
      }
    });
  }

  return {
    checkedWordCount: sourceWords.length,
    checkedEntryCount,
    issueCount: nearDuplicateMeanings.length,
    nearDuplicateMeanings
  };
}

