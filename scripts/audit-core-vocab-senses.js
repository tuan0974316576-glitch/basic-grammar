function auditApprovedBankMetadata(entries = [], expectedSource = "") {
  const validLevels = new Set(["A1", "A2", "B1", "B2", "C1", "C2"]);
  const exactKeys = new Map();
  const groupedByWordAndPos = new Map();
  const missingPos = [];
  const missingLevel = [];
  const invalidLevel = [];
  const missingDisplay = [];
  const missingSource = [];
  const invalidPos = [];
  const invalidType = [];
  const duplicateExact = [];
  const nearDuplicateMeanings = [];

  entries.forEach((entry) => {
    const label = `${entry.word} :: ${entry.pos || entry.type} :: ${entry.meaning}`;
    const normalizedPos = teacherVocab.normalizePos(entry.pos);
    const typeIssue = getStudentTypeIssue(entry);
    if (!entry.pos) {
      missingPos.push(label);
    } else if (!VALID_STUDENT_POS.has(normalizedPos)) {
      invalidPos.push(`${label} :: ${entry.pos || ""}`);
    }
    if (typeIssue) {
      invalidType.push(`${label} :: ${entry.type || ""}`);
    }
    if (!entry.level) {
      missingLevel.push(label);
    } else if (!validLevels.has(entry.level)) {
      invalidLevel.push(`${label} :: ${entry.level}`);
    }
    if (!entry.display) missingDisplay.push(label);
    const expectedSources = expectedSource === "curated-sense-bank"
      ? new Set(["curated-sense-bank", "verb-table-form"])
      : new Set([expectedSource]);
    if (!expectedSources.has(entry.source)) missingSource.push(`${label} :: ${entry.source || ""}`);

    const exactKey = [
      entry.word,
      entry.pos,
      entry.type,
      entry.meaning
    ].join("\t");
    exactKeys.set(exactKey, (exactKeys.get(exactKey) || 0) + 1);

    const groupKey = [
      teacherVocab.normalizeWord(entry.word),
      entry.pos || "",
      entry.type || "word"
    ].join("\t");
    if (!groupedByWordAndPos.has(groupKey)) groupedByWordAndPos.set(groupKey, []);
    groupedByWordAndPos.get(groupKey).push(entry);
  });

  exactKeys.forEach((count, key) => {
    if (count > 1) duplicateExact.push(`${key} :: ${count}`);
  });

  groupedByWordAndPos.forEach((group) => {
    for (let leftIndex = 0; leftIndex < group.length; leftIndex += 1) {
      for (let rightIndex = leftIndex + 1; rightIndex < group.length; rightIndex += 1) {
        const left = group[leftIndex];
        const right = group[rightIndex];
        const overlap = getMeaningPartOverlap(left.meaning, right.meaning);
        if (!overlap.isNearDuplicate) continue;
        nearDuplicateMeanings.push([
          `${left.word} :: ${left.pos || left.type}`,
          `${left.meaning}`,
          `${right.meaning}`,
          overlap.reason
        ].join(" :: "));
      }
    }
  });

  const issueCount = missingPos.length
    + missingLevel.length
    + invalidLevel.length
    + missingDisplay.length
    + missingSource.length
    + invalidPos.length
    + invalidType.length
    + duplicateExact.length
    + nearDuplicateMeanings.length;

  return {
    checkedEntryCount: entries.length,
    issueCount,
    missingPos,
    missingLevel,
    invalidLevel,
    missingDisplay,
    missingSource,
    invalidPos,
    invalidType,
    duplicateExact,
    nearDuplicateMeanings
  };
}

function normalizeMeaningPart(value = "") {
  return teacherVocab.normalizeMeaning(value)
    .replace(/[（）()「」『』]/g, "")
    .replace(/[的地]$/g, "")
    .replace(/[\s/／]+/g, "")
    .toLowerCase();
}

function getMeaningParts(value = "") {
  return teacherVocab.normalizeMeaning(value)
    .split(/\s*[/／]\s*/)
    .map(normalizeMeaningPart)
    .filter(Boolean);
}

function normalizeMeaningGroupKey(value = "") {
  return normalizeMeaningPart(value);
}

function getMeaningPartOverlap(leftMeaning = "", rightMeaning = "") {
  const leftParts = new Set(getMeaningParts(leftMeaning));
  const rightParts = new Set(getMeaningParts(rightMeaning));
  if (!leftParts.size || !rightParts.size) {
    return { isNearDuplicate: false, reason: "" };
  }
  const leftCovered = Array.from(leftParts).every((part) => rightParts.has(part));
  const rightCovered = Array.from(rightParts).every((part) => leftParts.has(part));
  if (leftCovered && rightCovered) {
    return { isNearDuplicate: true, reason: "same meaning parts" };
  }
  if (leftCovered || rightCovered) {
    return { isNearDuplicate: true, reason: "subset meaning parts" };
  }
  return { isNearDuplicate: false, reason: "" };
}

function auditExpectedDisplays(items = EXPECTED_DISPLAY_CASES, banks = [
  ...(senseBank.entries || []),
  ...(ccCedictSupplement.entries || [])
]) {
  const missing = [];
  const mismatched = [];
  items.forEach(([word, expectedDisplay]) => {
    const normalizedWord = teacherVocab.normalizeWord(word);
    const matches = banks.filter((entry) => teacherVocab.normalizeWord(entry.word) === normalizedWord);
    if (!matches.length) {
      missing.push(`${word} :: expected display ${expectedDisplay}`);
      return;
    }
    matches.forEach((entry) => {
      if (entry.display !== expectedDisplay) {
        mismatched.push(`${word} :: expected ${expectedDisplay} :: got ${entry.display || ""}`);
      }
    });
  });
  return {
    checkedWordCount: items.length,
    issueCount: missing.length + mismatched.length,
    missing,
    mismatched
  };
}

const TEACHER_EXTRACTION_JUNK_WORDS = new Set([
  "em",
  "f5 70%",
  "gar",
  "i a b t",
  "k a o",
  "l d o",
  "n/v",
  "t / i",
  "t c w",
  "to 在句頭",
  "v / n",
  "w / w"
]);

function isLikelyTeacherExtractionJunk(entry = {}) {
  const word = teacherVocab.normalizeWord(entry.word || entry.display || "");
  const display = String(entry.display || entry.word || "").trim();
  const meaning = teacherVocab.normalizeMeaning(entry.meaning || "");
  if (!word) return true;
  if (TEACHER_EXTRACTION_JUNK_WORDS.has(word)) return true;
  if (/^[a-z]$/.test(word)) return true;
  if (!/[a-z]{2,}/i.test(word)) return true;
  if (/^[-,.;:]+/.test(display)) return true;
  if (/[×]| x\d+\b/i.test(display)) return true;
  if (/^(?:[nv]|adj|adv|prep|conj)\.?$/i.test(word)) return true;
  if (/^[a-z]\s+(?:[a-z]\s+){1,}[a-z]$/i.test(word)) return true;
  if (/^[a-z]+$/.test(word) && meaning && !/[\u3400-\u9fff]/u.test(meaning)) return true;
  if (/[^\x00-\x7F]/.test(word)) return true;
  return false;
}

function auditTeacherExtractionJunkGaps(
  teacherEntries = teacherVocab.entries || [],
  curatedEntries = senseBank.entries || []
) {
  const curatedWords = new Set((curatedEntries || [])
    .flatMap((entry) => [
      teacherVocab.normalizeWord(entry.word || ""),
      ...(Array.isArray(entry.aliases) ? entry.aliases : []).map((alias) => teacherVocab.normalizeWord(alias || ""))
    ])
    .filter(Boolean));
  const rows = (teacherEntries || []).filter((entry) => entry.type !== "pattern" && !teacherVocab.normalizePos(entry.pos || ""));
  const missingNotCovered = rows.filter((entry) => !curatedWords.has(teacherVocab.normalizeWord(entry.word || entry.display || "")));
  const actionable = [];
  const suppressedJunk = [];
  const seen = new Set();

  missingNotCovered.forEach((entry) => {
    const word = teacherVocab.normalizeWord(entry.word || entry.display || "");
    const key = `${word}::${teacherVocab.normalizeMeaning(entry.meaning || "")}`;
    if (seen.has(key)) return;
    seen.add(key);
    const item = {
      word,
      display: entry.display || entry.word || word,
      meaning: entry.meaning || "",
      type: entry.type || "word",
      id: entry.id || ""
    };
    if (isLikelyTeacherExtractionJunk(entry)) {
      suppressedJunk.push(item);
    } else {
      actionable.push(item);
    }
  });

  return {
    checkedEntryCount: rows.length,
    missingNotCoveredCount: missingNotCovered.length,
    actionableCount: actionable.length,
    suppressedJunkCount: suppressedJunk.length,
    issueCount: actionable.length,
    actionable,
    suppressedJunk
  };
}

function auditStudentReadyLookupMetadata(words = null) {
  const issues = [];
  const validLevels = new Set(["A1", "A2", "B1", "B2", "C1", "C2"]);
  const exactKeys = new Map();
  let checkedEntryCount = 0;

  if (Array.isArray(words)) {
    words.forEach((word) => {
      const matches = teacherVocab.lookupStudentReady(word, { exactOnly: true, limit: 48 });
      matches.forEach((entry) => {
        checkedEntryCount += 1;
        auditStudentReadyEntry(entry, word, issues, validLevels, exactKeys);
      });
    });
  } else {
    const studentReadyEntries = teacherVocab.dedupeStudentReadyEntries(
      (teacherVocab.entries || []).map((entry) => teacherVocab.normalizeStudentReadyEntry(entry)).filter(Boolean)
    );
    studentReadyEntries.forEach((entry) => {
      checkedEntryCount += 1;
      auditStudentReadyEntry(entry, entry.word, issues, validLevels, exactKeys);
    });
  }

  const duplicateExact = [];
  exactKeys.forEach((count, key) => {
    if (count > 1) duplicateExact.push(`${key} :: ${count}`);
  });
  duplicateExact.forEach((duplicate) => issues.push(`${duplicate} :: duplicate-exact`));

  return {
    checkedWordCount: Array.isArray(words) ? words.length : null,
    checkedEntryCount,
    issueCount: issues.length,
    issues,
    duplicateExact
  };
}

function auditTeacherVisibleLabelDuplicateMetadata(words = null, lookupApi = teacherVocab) {
  const sourceWords = Array.isArray(words)
    ? getUniqueAuditWords(words)
    : Array.from(new Set((lookupApi.entries || []).map((entry) => lookupApi.normalizeWord(entry.word)).filter(Boolean)));
  const duplicateLabels = [];
  let checkedEntryCount = 0;

  sourceWords.forEach((word) => {
    const labelCounts = new Map();
    const matches = lookupApi.lookupStudentReady
      ? lookupApi.lookupStudentReady(word, { exactOnly: true, limit: 96 })
      : lookupApi.lookup(word, { exactOnly: true, limit: 96 });
    matches.forEach((entry) => {
      checkedEntryCount += 1;
      const label = lookupApi.getEntryLabel(entry);
      if (!label) return;
      labelCounts.set(label, (labelCounts.get(label) || 0) + 1);
    });
    labelCounts.forEach((count, label) => {
      if (count > 1) duplicateLabels.push(`${word} :: ${label} :: ${count}`);
    });
  });

  return {
    checkedWordCount: sourceWords.length,
    checkedEntryCount,
    issueCount: duplicateLabels.length,
    duplicateLabels
  };
}

function auditStudentReadyEntry(entry = {}, word = "", issues = [], validLevels = new Set(), exactKeys = new Map()) {
  const label = `${word} :: ${entry.pos || entry.type} :: ${entry.meaning}`;
  if (entry.source !== "teacher") issues.push(`${label} :: source=${entry.source || ""}`);
  if (!validLevels.has(entry.level)) issues.push(`${label} :: level=${entry.level || ""}`);
  const typeIssue = getStudentTypeIssue(entry);
  if (typeIssue) issues.push(`${label} :: invalid-type=${entry.type || ""}`);
  if (entry.type !== "pattern" && !teacherVocab.getStudentReadyPos(entry)) {
    issues.push(`${label} :: missing-pos`);
  } else if (entry.type !== "pattern" && !VALID_STUDENT_POS.has(teacherVocab.getStudentReadyPos(entry))) {
    issues.push(`${label} :: invalid-pos=${entry.pos || entry.inferredPos || ""}`);
  }
  if (/待老師|unknown|undefined|null/i.test(String(entry.meaning || ""))) {
    issues.push(`${label} :: placeholder`);
  }
  const exactKey = teacherVocab.getStudentReadyEntryKey
    ? teacherVocab.getStudentReadyEntryKey(entry)
    : [
      teacherVocab.normalizeWord(entry.word || entry.display),
      entry.pos || "",
      entry.type || "",
      teacherVocab.normalizeMeaning(entry.meaning)
    ].join("\t");
  exactKeys.set(exactKey, (exactKeys.get(exactKey) || 0) + 1);
}

