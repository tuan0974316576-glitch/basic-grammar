function normalizeOxfordLookupWord(entry = {}) {
  const rawWord = String(entry.word || entry.display || "").trim();
  const rawDisplay = String(entry.display || rawWord).trim();
  const candidate = rawWord || rawDisplay.replace(/\s*\([^)]*\)\s*$/u, "");
  const withoutSenseNumber = candidate.replace(/\d+$/u, "");
  const normalized = teacherVocab.normalizeWord(withoutSenseNumber);
  if (!normalized || !/[a-z]/i.test(normalized)) return "";
  if (/^[a-z]\.?[,]?$/i.test(normalized)) return "";
  if (/^(nction|tlement|v\.?,?)$/i.test(normalized)) return "";
  return withoutSenseNumber;
}

function getOxfordLookupWords(entry = {}) {
  const display = String(entry.display || "").trim();
  if (/^[A-Z][A-Z0-9&.-]*$/u.test(display) && display.length >= 2) {
    return Array.from(new Set([display, display.toLowerCase()]));
  }
  const lookupWord = normalizeOxfordLookupWord(entry);
  if (!lookupWord) return [];
  return lookupWord
    .split(/\s*,\s*/u)
    .map((part) => part.trim())
    .filter(Boolean);
}

async function auditOxfordLookupCoverage(entries = oxfordCefrVocab.entries || [], providers = getStudentLookupProviders()) {
  const checkedWords = new Map();
  const missing = [];
  const missingPos = [];
  let skippedJunkCount = 0;

  for (const entry of entries) {
    const lookupWords = getOxfordLookupWords(entry);
    if (!lookupWords.length) {
      skippedJunkCount += 1;
      continue;
    }
    const matchGroups = [];
    const hasAnyMatch = await lookupWords.reduce(async (previous, lookupWord) => {
      const previousMatched = await previous;
      const cacheKey = String(lookupWord || "").trim();
      let matches = checkedWords.get(cacheKey);
      if (!matches) {
        matches = await buildStudentLookupMatches(lookupWord, providers);
        checkedWords.set(cacheKey, matches);
      }
      matchGroups.push({ lookupWord, matches });
      return previousMatched || matches.length > 0;
    }, Promise.resolve(false));
    if (!hasAnyMatch) {
      missing.push({
        word: lookupWords.join(", "),
        display: entry.display || entry.word || "",
        level: entry.level || "",
        pos: entry.pos || []
      });
      continue;
    }
    const normalizedOxfordWord = teacherVocab.normalizeWord(entry.word || entry.display || "");
    const expectedPos = Array.from(new Set((entry.pos || []).map((pos) => teacherVocab.normalizePos(pos)).filter(Boolean)))
      .filter((pos) => !(
        normalizedOxfordWord === "subject"
        && pos === "adjective"
      ));
    const actualPos = Array.from(new Set(matchGroups.flatMap((group) => (
      group.matches.map((match) => teacherVocab.normalizePos(match.pos || match.inferredPos)).filter(Boolean)
    ))));
    const missingExpectedPos = expectedPos.filter((pos) => !actualPos.includes(pos));
    if (missingExpectedPos.length) {
      missingPos.push({
        word: lookupWords.join(", "),
        display: entry.display || entry.word || "",
        level: entry.level || "",
        expectedPos: missingExpectedPos,
        actualPos
      });
    }
  }

  return {
    checkedEntryCount: entries.length,
    checkedWordCount: checkedWords.size,
    skippedJunkCount,
    issueCount: missing.length,
    posGapCount: missingPos.length,
    missing,
    missingPos
  };
}

